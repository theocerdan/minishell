#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	unsigned int	total;
	unsigned int	i;
	char			*result;

	total = count * size;
	result = malloc(total);
	if (!result)
		return (NULL);
	i = 0;
	while (total != 0)
	{
		result[i] = 0;
		total--;
		i++;
	}
	return ((void *)result);
}
